package com.eknm.model;

/**
 * Class to distinguish  operation with numbers
 */
public enum Operator {

    /**
     * First operation
     */
    PERCENT,

    /**
     * Second operation
     */
    SQRT,

    /**
     * Third operation
     */
    SECOND_POWER,

    /**
     * Fourth operation
     */
    INVERSE,

    /**
     * Fifth operation
     */
    DIVISION,

    /**
     * Sixth operation
     */
    MULTIPLY,

    /**
     * Seventh operation
     */
    MINUS,

    /**
     * Eighth operation
     */
    PLUS,

    /**
     * Ninth operation
     */
    CHANGE_SIGN;




}
